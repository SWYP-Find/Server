        WHERE UPPER(COALESCE(b.type, 'BATTLE')) = 'QUIZ'
          AND NOT EXISTS (SELECT 1 FROM quiz_options qo WHERE qo.id = bo.id);

        INSERT INTO poll_options (id, poll_id, label, title, display_order, vote_count, created_at, updated_at)
        SELECT
            bo.id,
            bo.battle_id,
            bo.label,
            COALESCE(NULLIF(bo.title, ''),
                     CASE bo.label WHEN 'A' THEN b.item_a WHEN 'B' THEN b.item_b ELSE NULL END,
                     '선택지'),
            COALESCE(bo.display_order, CASE bo.label WHEN 'A' THEN 1 WHEN 'B' THEN 2 WHEN 'C' THEN 3 WHEN 'D' THEN 4 ELSE 99 END),
            COALESCE(bo.vote_count, 0),
            bo.created_at,
            bo.updated_at
        FROM battle_options bo
        JOIN battles b ON b.id = bo.battle_id
        WHERE UPPER(COALESCE(b.type, 'BATTLE')) IN ('POLL', 'VOTE')
          AND NOT EXISTS (SELECT 1 FROM poll_options po WHERE po.id = bo.id);
    END IF;
END $$;

-- 5) Migrate votes(votes table) into quiz_user_votes / poll_user_votes
DO $$
BEGIN
    IF to_regclass('public.votes') IS NOT NULL
        AND to_regclass('public.battles') IS NOT NULL
        AND to_regclass('public.quiz_options') IS NOT NULL THEN

        INSERT INTO quiz_user_votes (user_id, quiz_id, option_id, created_at, updated_at)
        SELECT
            src.user_id,
            src.quiz_id,
            src.option_id,
            src.created_at,
            src.updated_at
        FROM (
            SELECT DISTINCT ON (v.user_id, v.battle_id)
                v.user_id,
                v.battle_id AS quiz_id,
                COALESCE(v.post_vote_option_id, v.pre_vote_option_id) AS option_id,
                v.created_at,
                v.updated_at
            FROM votes v
            JOIN battles b ON b.id = v.battle_id
            WHERE UPPER(COALESCE(b.type, 'BATTLE')) = 'QUIZ'
              AND COALESCE(v.post_vote_option_id, v.pre_vote_option_id) IS NOT NULL
            ORDER BY v.user_id, v.battle_id, v.updated_at DESC, v.id DESC
        ) src
        JOIN quiz_options qo ON qo.id = src.option_id AND qo.quiz_id = src.quiz_id
        ON CONFLICT (user_id, quiz_id) DO UPDATE
        SET option_id = EXCLUDED.option_id,
            updated_at = EXCLUDED.updated_at;
    END IF;

    IF to_regclass('public.votes') IS NOT NULL
        AND to_regclass('public.battles') IS NOT NULL
        AND to_regclass('public.poll_options') IS NOT NULL THEN

        INSERT INTO poll_user_votes (user_id, poll_id, option_id, created_at, updated_at)
        SELECT
            src.user_id,
            src.poll_id,
            src.option_id,
            src.created_at,
            src.updated_at
        FROM (
            SELECT DISTINCT ON (v.user_id, v.battle_id)
                v.user_id,
                v.battle_id AS poll_id,
                COALESCE(v.post_vote_option_id, v.pre_vote_option_id) AS option_id,
                v.created_at,
                v.updated_at
            FROM votes v
            JOIN battles b ON b.id = v.battle_id
            WHERE UPPER(COALESCE(b.type, 'BATTLE')) IN ('POLL', 'VOTE')
              AND COALESCE(v.post_vote_option_id, v.pre_vote_option_id) IS NOT NULL
            ORDER BY v.user_id, v.battle_id, v.updated_at DESC, v.id DESC
        ) src
        JOIN poll_options po ON po.id = src.option_id AND po.poll_id = src.poll_id
        ON CONFLICT (user_id, poll_id) DO UPDATE
        SET option_id = EXCLUDED.option_id,
            updated_at = EXCLUDED.updated_at;
    END IF;
END $$;

-- 6) Recalculate counters
WITH quiz_counts AS (
    SELECT quiz_id, COUNT(*)::BIGINT AS cnt
    FROM quiz_user_votes
    GROUP BY quiz_id
)
UPDATE quizzes q
SET total_participants_count = qc.cnt
FROM quiz_counts qc
WHERE q.id = qc.quiz_id
  AND q.total_participants_count IS DISTINCT FROM qc.cnt;

UPDATE quizzes q
SET total_participants_count = 0
WHERE q.total_participants_count IS DISTINCT FROM 0
  AND NOT EXISTS (SELECT 1 FROM quiz_user_votes qv WHERE qv.quiz_id = q.id);

WITH poll_counts AS (
    SELECT poll_id, COUNT(*)::BIGINT AS cnt
    FROM poll_user_votes
    GROUP BY poll_id
)
UPDATE poll_contents p
SET total_participants_count = pc.cnt
FROM poll_counts pc
WHERE p.id = pc.poll_id
  AND p.total_participants_count IS DISTINCT FROM pc.cnt;

UPDATE poll_contents p
SET total_participants_count = 0
WHERE p.total_participants_count IS DISTINCT FROM 0
  AND NOT EXISTS (SELECT 1 FROM poll_user_votes pv WHERE pv.poll_id = p.id);

WITH option_counts AS (
    SELECT option_id, COUNT(*)::BIGINT AS cnt
    FROM poll_user_votes
    GROUP BY option_id
)
UPDATE poll_options po
SET vote_count = oc.cnt
FROM option_counts oc
WHERE po.id = oc.option_id
  AND po.vote_count IS DISTINCT FROM oc.cnt;

UPDATE poll_options po
SET vote_count = 0
WHERE po.vote_count IS DISTINCT FROM 0
  AND NOT EXISTS (SELECT 1 FROM poll_user_votes pv WHERE pv.option_id = po.id);

