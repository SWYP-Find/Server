-- Split migration: monolith battles(type=QUIZ/POLL/VOTE) -> quizzes / poll_contents
-- This script is idempotent for partially migrated environments.

-- 0) Create target tables
CREATE TABLE IF NOT EXISTS quizzes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(200) NOT NULL,
    target_date DATE,
    total_participants_count BIGINT NOT NULL DEFAULT 0,
    status VARCHAR(20) NOT NULL,
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS quiz_options (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    quiz_id BIGINT NOT NULL,
    label VARCHAR(10) NOT NULL,
    text VARCHAR(300) NOT NULL,
    detail_text VARCHAR(1000),
    is_correct BOOLEAN NOT NULL DEFAULT FALSE,
    display_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS poll_contents (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title_prefix VARCHAR(200) NOT NULL,
    title_suffix VARCHAR(200),
    target_date DATE,
    total_participants_count BIGINT NOT NULL DEFAULT 0,
    status VARCHAR(20) NOT NULL,
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS poll_options (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    poll_id BIGINT NOT NULL,
    label VARCHAR(10) NOT NULL,
    title VARCHAR(200) NOT NULL,
    display_order INTEGER NOT NULL DEFAULT 0,
    vote_count BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS quiz_user_votes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    quiz_id BIGINT NOT NULL,
    option_id BIGINT NOT NULL,
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

CREATE TABLE IF NOT EXISTS poll_user_votes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    poll_id BIGINT NOT NULL,
    option_id BIGINT NOT NULL,
    created_at TIMESTAMP,
    updated_at TIMESTAMP
);

-- 1) Constraints and indexes
DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_quiz_options_quiz') THEN
        ALTER TABLE quiz_options
            ADD CONSTRAINT fk_quiz_options_quiz
            FOREIGN KEY (quiz_id) REFERENCES quizzes(id) ON DELETE CASCADE;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_poll_options_poll') THEN
        ALTER TABLE poll_options
            ADD CONSTRAINT fk_poll_options_poll
            FOREIGN KEY (poll_id) REFERENCES poll_contents(id) ON DELETE CASCADE;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_quiz_user_votes_user') THEN
        ALTER TABLE quiz_user_votes
            ADD CONSTRAINT fk_quiz_user_votes_user
            FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_quiz_user_votes_quiz') THEN
        ALTER TABLE quiz_user_votes
            ADD CONSTRAINT fk_quiz_user_votes_quiz
            FOREIGN KEY (quiz_id) REFERENCES quizzes(id) ON DELETE CASCADE;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_quiz_user_votes_option') THEN
        ALTER TABLE quiz_user_votes
            ADD CONSTRAINT fk_quiz_user_votes_option
            FOREIGN KEY (option_id) REFERENCES quiz_options(id) ON DELETE CASCADE;
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_poll_user_votes_user') THEN
        ALTER TABLE poll_user_votes
            ADD CONSTRAINT fk_poll_user_votes_user
            FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_poll_user_votes_poll') THEN
        ALTER TABLE poll_user_votes
            ADD CONSTRAINT fk_poll_user_votes_poll
            FOREIGN KEY (poll_id) REFERENCES poll_contents(id) ON DELETE CASCADE;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'fk_poll_user_votes_option') THEN
        ALTER TABLE poll_user_votes
            ADD CONSTRAINT fk_poll_user_votes_option
            FOREIGN KEY (option_id) REFERENCES poll_options(id) ON DELETE CASCADE;
    END IF;
END $$;

