                 WHEN b.status = 'ARCHIVED' THEN 'ARCHIVED'
                 ELSE 'PENDING' END,
            b.created_at,
            b.updated_at
        FROM battles b
        WHERE UPPER(COALESCE(b.type, 'BATTLE')) = 'QUIZ'
          AND NOT EXISTS (SELECT 1 FROM quizzes q WHERE q.id = b.id);

        INSERT INTO poll_contents (id, title_prefix, title_suffix, target_date, total_participants_count, status, created_at, updated_at)
        SELECT
            b.id,
            COALESCE(NULLIF(b.title_prefix, ''), NULLIF(b.title, ''), '투표'),
            NULLIF(b.title_suffix, ''),
            b.target_date,
            COALESCE(b.total_participants, 0),
            CASE WHEN b.status = 'PUBLISHED' THEN 'PUBLISHED'
                 WHEN b.status = 'ARCHIVED' THEN 'ARCHIVED'
                 ELSE 'PENDING' END,
            b.created_at,
            b.updated_at
        FROM battles b
        WHERE UPPER(COALESCE(b.type, 'BATTLE')) IN ('POLL', 'VOTE')
          AND NOT EXISTS (SELECT 1 FROM poll_contents p WHERE p.id = b.id);
    END IF;

    IF to_regclass('public.battle_options') IS NOT NULL AND to_regclass('public.battles') IS NOT NULL THEN
        INSERT INTO quiz_options (id, quiz_id, label, text, detail_text, is_correct, display_order, created_at, updated_at)
        SELECT
            bo.id,
            bo.battle_id,
            bo.label,
            COALESCE(NULLIF(bo.title, ''),
                     CASE bo.label WHEN 'A' THEN b.item_a WHEN 'B' THEN b.item_b ELSE NULL END,
                     '선택지'),
            COALESCE(NULLIF(bo.quote, ''),
                     CASE bo.label WHEN 'A' THEN b.item_a_desc WHEN 'B' THEN b.item_b_desc ELSE NULL END),
